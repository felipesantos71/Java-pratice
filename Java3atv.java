public class Java3atv {

    public static void main(String[] args) {
        // Declaração de variaveis
        int a = 10;
        int b = 2;
        int maior;
       
        //Estrutura condicional if simples e operadores aritmeticos
        if (a > b) {
            maior = a; 
        }   else    {
            maior = b;
        }
        
        //Exibindo resultados
        System.out.println("O maior número é: " + maior);
    }
}
